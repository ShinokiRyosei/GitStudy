import UIKit

extension UIColor {
    class var lightGreen: UIColor { return #colorLiteral(red: 0.7843137255, green: 0.9019607843, blue: 0.7882352941, alpha: 1) }
    class var middleGreen: UIColor { return #colorLiteral(red: 0.4, green: 0.7333333333, blue: 0.4156862745, alpha: 1) }
    class var darkGreen: UIColor { return #colorLiteral(red: 0.1803921569, green: 0.4901960784, blue: 0.1960784314, alpha: 1) }
    
    class var blackTheme: UIColor { return #colorLiteral(red: 0.1294117647, green: 0.1294117647, blue: 0.1294117647, alpha: 1) }
    class var whiteTheme: UIColor { return #colorLiteral(red: 0.9803921569, green: 0.9803921569, blue: 0.9803921569, alpha: 1) }
}
